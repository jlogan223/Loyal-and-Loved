// Helper functions
window.getArticleBySlug = function(slug) {
  return window.LNL_ARTICLES.find(a => a.slug === slug);
};

window.getArticlesByCategory = function(category) {
  return window.LNL_ARTICLES.filter(a => a.category === category);
};

window.getFeaturedArticles = function() {
  return window.LNL_ARTICLES.filter(a => a.featured);
};

window.getLatestArticles = function(limit = 12) {
  // Articles are listed in reverse chronological order
  return window.LNL_ARTICLES.slice(0, limit);
};

window.getRelatedArticles = function(currentSlug, limit = 3) {
  const current = window.getArticleBySlug(currentSlug);
  if (!current) return [];

  return window.LNL_ARTICLES
    .filter(a => a.category === current.category && a.slug !== currentSlug)
    .slice(0, limit);
};
